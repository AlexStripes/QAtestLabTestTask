package model;

/**
 *
 * @author Stripes
 */
public class Date {
    private int day;
    private int hour;

    /**
     * @return the day
     */
    public int getDay() {
        return day;
    }

    /**
     * @param day the day to set
     */
    public void setDay(int day) {
        this.day = day;
    }

    /**
     * @return the hour
     */
    public int getHour() {
        return hour;
    }

    /**
     * @param hour the hour to set
     */
    public void setHour(int hour) {
        this.hour = hour;
    }

    public Date(){
        this.day=1;
        this.hour=8;
    }
    
    public void NextDay(){
        this.day++;
        this.hour=8;
    }
}

